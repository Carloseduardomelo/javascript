//atribuição de valores em uma variavel
var valor = 10
console.log(valor)

// atribuição de += 
valor += 10 // Ele ta atribuino mais dez na variavel valor
console.log(valor)

// atribuição -=
valor -= 2 // Ele ta tirando dois da variavel valor
console.log(valor)

// atribuição *=
valor *= 3 // A variavel valor  esta sendo mutiplicado por tres 
console.log(valor)

// atribuição /= 
valor /= 2 // A variavel avlor esta sendo dividida por dois
console.log(valor)

