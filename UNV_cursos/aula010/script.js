let valor = 10

// operadores aritiméticos
// + = adição
// - = subtração
// * = multiplicação
// / = divisão
// ** = potencia

// adoção
valor = 10 + 10
console.log(valor)

// subtração
valor = 10 - 5
console.log(valor)

//multiplicação
valor = 10 * 10
console.log(valor)

//divisão
valor = 10 / 2
console.log(valor)

//potencia
valor = 5 ** 5
console.log(valor)

// operadores de incrementação
// ++
// --
let idade = 18

// ++ 
console.log(++idade)
//aqui ele adicionar mais 1 a 18

// --
console.log(--idade)
// aqui ele vai tira 1 de 18



// exenplo de operadores aritiméticos e de incrementação