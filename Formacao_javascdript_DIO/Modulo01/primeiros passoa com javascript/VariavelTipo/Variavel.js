//! para declara uma variavel nos precizanos de umas palavras expecificas para dixer que aquele nome e uma variavel.

//! São eles  let , var e const esses são a3 principais formar de determinar um variavel

//todo / Example


var nome = "carlos" //* Pode ver mudada
console.log(nome)

let nomero = 10 //* Pode ser mudad
console.log(nomero)

const sexo = "masculino" //* No caso da constande ela não pode ser mudad por que ela e uma constante.
console.log(sexo)

