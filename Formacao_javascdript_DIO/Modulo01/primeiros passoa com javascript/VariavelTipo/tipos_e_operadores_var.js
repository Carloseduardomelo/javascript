//! No javascript temos varios tipos de operadores os mais usados são os matematicos que são eles.

/*
+ = Adiçao
- = Subibração
* = Multiplicação
/ = Divição
** = Potencia
%  = resto da divição
*/

//todo exemple

var numero = 10

//! adição
console.log(numero + 10)

//! subtração
console.log(numero - 10)

//! multiplicação
console.log(numero * 10)

//! divisão
console.log(numero / 10)

//! potencia
console.log(numero ** 2)

//! resto da divisão
console.log(numero % 10)


//! algums exemplos de operadores